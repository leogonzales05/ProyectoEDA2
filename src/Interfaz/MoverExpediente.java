    private void botonMoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMoverActionPerformed
        // TODO add your handling code here:
try{
        int idExpediente= Integer.parseInt(textIdExpediente.getText());
        String origen=textOrigen.getText();
        String destino=textDestino.getText();
        if (origen.isEmpty() || destino.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Se deben ingresar todos los datos solicitados", 
                "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (origen.equals(destino)) {
            JOptionPane.showMessageDialog(this, 
                "El origen y destino no pueden ser iguales", 
                "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Seguimiento cambio = new Seguimiento(origen, destino, Fecha.Exacta());
        boolean cambioExitoso = Programa.cambiarExpediente(idExpediente, cambio);
        if (cambioExitoso) {
            JOptionPane.showMessageDialog(this, 
                "Expediente movido exitosamente de " + origen + " a " + destino, 
                "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, 
                "No se pudo mover el expediente", 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, 
            "Verifique que el ID ingresado sea correcto.", 
            "Error", JOptionPane.ERROR_MESSAGE);
    }
       
    }//GEN-LAST:event_botonMoverActionPerformed

